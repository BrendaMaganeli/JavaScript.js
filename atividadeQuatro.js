//4) Programar a conversão de uma temperatura digitada pelo usuário em graus Celsius para Fahrenheit. Mostrar o resultado na tela.

// variáveis

let temperaturaDigitada
let conversaoCelsiusParaFahrenheit

// entrada de dados

temperaturaDigitada = prompt('Digite a temperatura que deseja converter: ')

// cálculos

conversaoCelsiusParaFahrenheit = temperaturaDigitada *  9/5 + 32 



// saída de dados

alert('temperatura em farhenheit: ' + conversaoCelsiusParaFahrenheit)

